# Because Hello, is in single quotations with no other special characters, here it is just a string
# so the len function will just count the number of characters in the string
tup = 'Hello'
print(len(tup))
print(tup)
print('')

# in this example, because of the comma (,) after Hello, tup is now a tuple
# the len function will now count how many elements are in the list
tup = 'Hello',
print(len(tup))
print(tup)

