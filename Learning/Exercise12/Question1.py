cheese = ['Cheddar', 'Stilton', 'Cornish Yarg']
print(cheese)
print('')
# Add to the list with the following code:
print('Add a new element to the list')
cheese.append('Oke')
print(cheese)
print('')
# Add 2 new elements to the list with one line of code
# append can only add one item at a time, EXTEND can add more
print('Add 2 new elements')
cheese.extend(['Emental', 'Red Leicester'])
print(cheese)
