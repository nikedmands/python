# makes a prompt in the console and get a reply
var = input("Please enter a value: ")
# to print in upper case it needs a METHOD
print(var.upper())
# counting the digits DOESN'T require a method
print(len(var))
# contain any numerical characters, needs the .isdecimal() METHOD.
# this returns TRUE/FALSE (i.e. all characters would have to be numbers)
print(var.isdecimal())





